class Student{
    private String name;
    private int rno;
    // setter methods
    public void setName(String name){
        this.name=name;
    } 
    public void setRno(int rno){
        this.rno=rno;
    } 
    // getter methods
    public String getName(){
        return name;
    }
    public int getRno(){
        return rno;
    }
}