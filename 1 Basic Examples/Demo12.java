// Type casting

class Demo12{
    public static void main(String args[]){
        short a = 130;
        short b = 30;
        int c = a+b;
        short d = (short)(a+b);

        System.out.println("a : "+a);
        System.out.println("b : "+b);
        System.out.println("c : "+c);
        System.out.println("d : "+d);        
    }
}















 