// Type casting

class Demo10{
    public static void main(String args[]){
        int a = 5;
        double b = a; // implicit type casting

        System.out.println("a : "+a);
        System.out.println("b : "+b);
        
        double c = 10;
        int d = (int)c; // explicit type casting
        
        System.out.println("c : "+c);
        System.out.println("d : "+d);
        
    }
}

