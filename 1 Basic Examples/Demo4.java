class Demo4{
    public static void main(String args[]){
        int a = 100;
        System.out.println("value of a : "+a);

        int b = 012;
        System.out.println("value of b : "+b);

        int c = 0x12;
        System.out.println("value of c : "+c);

        int d = 0xa12;
        System.out.println("value of d : "+d);

        int e = 0xA12;
        System.out.println("value of e : "+e);

    }
}

