// Type casting

class Demo11{
    public static void main(String args[]){
        byte a = (byte)130;
        byte b = 30;
        int c = a+b;
        byte d = (byte)(a+b);

        System.out.println("a : "+a);
        System.out.println("b : "+b);
        System.out.println("c : "+c);
        System.out.println("d : "+d);        
    }
}

